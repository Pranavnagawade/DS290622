"""
Arithemetic Operator

+ - addition
- - subtraction
* - multiplication
/ - division
% - modulus (returns remainder)
** - exponentia 
/ /  - floor division
"""

num1 = 25
num2 = 10
add = num1 + num2
print("Addition : ",add)

sub = num1 - num2
print("Subtraction : ",sub)

mul = num1 * num2
print("Multiplication : ",mul)

div = num1/num2 # / division operator always returns the float value
print("Division : ",div)

floor_div = num1//num2
print("Floor Division : ",floor_div)

mod = num1 % num2 # it returns remainder
print("Modulus : ",mod)

expo = num1 ** 2
print("Exponentia : ",expo)
 